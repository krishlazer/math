package com.krish.mathconcept;

public class Cube 
{

	private int CubeSide;
	private int CubeArea;
	private int CubeVolume;

	
	public int calculateDimensions()
	{
		
		System.out.println("The dimensions of a cube can be listed as : ");
		return CubeSide*CubeArea*CubeVolume;
		
		
	}



}
